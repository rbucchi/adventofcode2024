static bool CheckStar(char[,] input, int i, int j)
{
    if (j + 2 >= input.GetLength(0)) return false;
    if (i + 2 >= input.GetLength(1)) return false;
    if (input[i, j] != 'M') return false;
    if (input[i, j + 2] != 'S') return false;
    if (input[i + 1, j + 1] != 'A') return false;
    if (input[i + 2, j] != 'M') return false;
    if (input[i + 2, j + 2] != 'S') return false;
    return true;
}

