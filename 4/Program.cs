static bool CheckHorizontal(char[,] input, int i, int j)
{
    if (j + 3 >= input.GetLength(0)) return false;
    if (input[i, j] != 'X') return false;
    if (input[i, j + 1] != 'M') return false;
    if (input[i, j + 2] != 'A') return false;
    if (input[i, j + 3] != 'S') return false;
    return true;
}
static bool CheckHorizontalBackwards(char[,] input, int i, int j)
{
    if (j - 3 < 0) return false;
    if (input[i, j] != 'X') return false;
    if (input[i, j - 1] != 'M') return false;
    if (input[i, j - 2] != 'A') return false;
