def read_matrix_from_string(input_string: str) -> List[List[str]]:
    """read input and transform it as matrix """
    print("Reading matrix from input string:\n")
    lines = input_string.strip().split("\n")
    matrix = [list(line) for line in lines]
    return matrix

def print_matrix(matrix: List[List[str]]) -> None:
    """print a matrix"""
    print("\nThe matrix is:")
    for row in matrix:
        print(''.join(row))

def find_areas(matrix: List[List[str]]) -> List[Tuple[str, List[Tuple[int, int]]]]:
    visited = [[False for _ in row] for row in matrix]
    areas = []

    def dfs(x, y, letter, current_area):
        if x < 0 or y < 0 or x >= len(matrix) or y >= len(matrix[0]):
            return
        if visited[x][y] or matrix[x][y] != letter:
            return

        visited[x][y] = True
        current_area.append((x, y))

        # Explore 4-connected neighbors
        dfs(x + 1, y, letter, current_area)
        dfs(x - 1, y, letter, current_area)
        dfs(x, y + 1, letter, current_area)
        dfs(x, y - 1, letter, current_area)

    for i in range(len(matrix)):
        for j in range(len(matrix[0])):
            if not visited[i][j]:
                current_area = []
                dfs(i, j, matrix[i][j], current_area)
                if current_area:
                    areas.append((matrix[i][j], current_area))

    return areas

