def calculate_perimeter(matrix: List[List[str]], positions: List[Tuple[int, int]]) -> int:
    perimeter = 0
    rows, cols = len(matrix), len(matrix[0])

    for x, y in positions:
        # Check all 4 sides (up, down, left, right)
        if x == 0 or matrix[x - 1][y] != matrix[x][y]:
            perimeter += 1  # Top
        if x == rows - 1 or matrix[x + 1][y] != matrix[x][y]:
            perimeter += 1  # Bottom
        if y == 0 or matrix[x][y - 1] != matrix[x][y]:
            perimeter += 1  # Left
        if y == cols - 1 or matrix[x][y + 1] != matrix[x][y]:
            perimeter += 1  # Right

    return perimeter


